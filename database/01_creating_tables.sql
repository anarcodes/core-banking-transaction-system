-----------------------------------------
-- 1. Currencies table (Valyuta cedveli)
-----------------------------------------

CREATE TABLE currencies (
    currency_code CHAR(3), -- AZN, USD, EUR
    currency_name VARCHAR2(50) NOT NULL,
    exchange_rate NUMBER(10,4) DEFAULT 1 NOT NULL, -- [EN] AZN ratio | [AZ] AZN-e nisbet
    last_updated TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT pk_currency PRIMARY KEY (currency_code)
);

